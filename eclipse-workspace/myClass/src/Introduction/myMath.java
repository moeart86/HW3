package Introduction;

public class myMath {
	/* java has a class call "Math" which allows you to perform  Mathematical action on variables or values */
	
	
	
	
	public static void main(String[] args) {
		
	double x = 2.5;
	
	int y = 20;
	
	System.out.println("the smaller value is "+ Math.min(x, y));//Math.min - to find minimum between two number
	
	System.out.println("the larger value is " + Math.max(x, y));//Math.max - to find maximum between two number
	
	
	System.out.println("Random number is " + Math.random());//This generates random number 
	
	System.out.println("square root is " + Math.sqrt(49));//square root
	
	}

}
